  - "INSERT INTO DBT_DEMO.AUDIT.AUDIT_LOG (INVOCATION_ID,MODEL_NAME,STATUS,START_TIME,END_TIME,USER_NAME) VALUES ('{{ invocation_id }}','ON-RUN-END','COMPLETED',NULL,CURRENT_TIMESTAMP,'{{ target.user }}')"	
